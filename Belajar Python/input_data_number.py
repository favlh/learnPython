# Belajar input data number

print("Masukkan angka sesuka hati kalian")

angka_pertama = int(input("masukkan nomor pertama: "))
angka_kedua = int(input("masukkan nomor kedua: "))
angka_ketiga = int(input("masukkan nomor ketiga: "))

hasil_pertama = angka_pertama + angka_kedua - angka_ketiga
hasil_kedua = angka_pertama * angka_kedua / angka_ketiga

print(f"hasil pertamanya adalah: {hasil_pertama}")
print(f"hasil keduanya adalah: {hasil_kedua}")